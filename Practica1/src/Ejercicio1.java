public class Ejercicio1 {
    public static void main(String[] args) {
        int x = 144;
        int y = 999;

        System.out.println("Valor de x:" + x);
        System.out.println("Valor de y:" + y);

        int suma = x + y;
        System.out.println("El resultado de la suma da:" + suma);

        int resta = x - y;
        System.out.println("El resultado de la resta da:" + resta);

        int multiplicacion = x * y;
        System.out.println("El resultado de la multiplicacion da:" + multiplicacion);

        double division = (double) x / y;
        System.out.println("El resultado de la division da:" + division);

    }
}
